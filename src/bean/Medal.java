package bean;

public class Medal {

	public int gold=0,silver=0,bronze=0;
	
    public void setGold(int gold) {
    	this.gold = gold;
    }
    
    public int getGold() {
    	return gold;
    }
    public void setSilver(int silver) {
    	this.silver = silver;
    }
    
    public int getSilver() {
    	return silver;
    }
    public void setBronze(int bronze) {
    	this.bronze = bronze;
    }
    
    public int getBronze() {
    	return bronze;
    }
}
